static RedfishCS_status CS_To_JSON_InterleavableMemorySets(json_t *CsJson, char *Key, RedfishMemoryDomain_V1_1_0_MemorySet_Array_CS *CSPtr)
{
  json_t *ArrayJson;
  json_t *ArrayMember;
  RedfishMemoryDomain_V1_1_0_MemorySet_Array_CS *NextArray;
  RedfishMemoryDomain_V1_1_0_MemorySet_CS *NextArrayItem;

  if (CSPtr == NULL) {
    return RedfishCS_status_success;
  }
  ArrayJson = json_array();
  if (ArrayJson == NULL) {
    return RedfishCS_status_unsupported;
  }
  NextArray = CSPtr;
  do {
    ArrayMember = json_object();
    if (ArrayMember == NULL) {
      return RedfishCS_status_unsupported;
    }

    NextArrayItem = NextArray->ArrayValue;
    // MemorySet 
    if (InsertJsonLinkArrayObj (ArrayMember, "MemorySet", &NextArrayItem->MemorySet) != RedfishCS_status_success) {goto Error;}

    // MemorySet@odata.count 
    if (InsertJsonInt64Obj (ArrayMember, "MemorySet@odata.count", NextArrayItem->MemorySetodata_count) != RedfishCS_status_success) {goto Error;}

    // MemorySet@odata.navigationLink 
    if (InsertJsonStringObj (ArrayMember, "MemorySet@odata.navigationLink", NextArrayItem->MemorySetodata_navigationLink) != RedfishCS_status_success) {goto Error;}

    if (json_array_append_new (ArrayJson, ArrayMember) != 0) {goto Error;}
    NextArray = NextArray->Next;
  } while (NextArray != NULL);
  json_object_set_new (CsJson, Key, ArrayJson);

  return RedfishCS_status_success;
Error:;
  return RedfishCS_status_unsupported;
}

//
// C structure to JSON for MemoryDomain.V1_1_0.MemoryDomain
//
RedfishCS_status CS_To_MemoryDomain_V1_1_0_JSON(RedfishMemoryDomain_V1_1_0_MemoryDomain_CS *CSPtr, RedfishCS_char **JsonText)
{
  json_t  *CsJson;

  if (CSPtr == NULL || JsonText == NULL || CSPtr->Header.ResourceType != RedfishCS_Type_CS) {
    return RedfishCS_status_invalid_parameter;
  }
  CsJson = json_object();
  if (CsJson == NULL) {
    return RedfishCS_status_unsupported;
  }
  // AllowsBlockProvisioning 
  if (InsertJsonBoolObj (CsJson, "AllowsBlockProvisioning", CSPtr->AllowsBlockProvisioning) != RedfishCS_status_success) {goto Error;}

  // AllowsMemoryChunkCreation 
  if (InsertJsonBoolObj (CsJson, "AllowsMemoryChunkCreation", CSPtr->AllowsMemoryChunkCreation) != RedfishCS_status_success) {goto Error;}

  // AllowsMirroring 
  if (InsertJsonBoolObj (CsJson, "AllowsMirroring", CSPtr->AllowsMirroring) != RedfishCS_status_success) {goto Error;}

  // AllowsSparing 
  if (InsertJsonBoolObj (CsJson, "AllowsSparing", CSPtr->AllowsSparing) != RedfishCS_status_success) {goto Error;}

  // Description 
  if (InsertJsonStringObj (CsJson, "Description", CSPtr->Description) != RedfishCS_status_success) {goto Error;}

  // Id 
  if (InsertJsonStringObj (CsJson, "Id", CSPtr->Id) != RedfishCS_status_success) {goto Error;}

  // InterleavableMemorySets
  if (CS_To_JSON_InterleavableMemorySets(CsJson, "InterleavableMemorySets", CSPtr->InterleavableMemorySets) != RedfishCS_status_success) {goto Error;}

  // MemoryChunks 
  if (InsertJsonLinkObj (CsJson, "MemoryChunks", &CSPtr->MemoryChunks) != RedfishCS_status_success) {goto Error;}

  // Name 
  if (InsertJsonStringObj (CsJson, "Name", CSPtr->Name) != RedfishCS_status_success) {goto Error;}

  // Oem
  if (CS_To_JSON_Oem(CsJson, "Oem", CSPtr->Oem) != RedfishCS_status_success) {goto Error;}

  // @odata.context 
  if (InsertJsonStringObj (CsJson, "@odata.context", CSPtr->odata_context) != RedfishCS_status_success) {goto Error;}

  // @odata.id 
  if (InsertJsonStringObj (CsJson, "@odata.id", CSPtr->odata_id) != RedfishCS_status_success) {goto Error;}

  // @odata.type 
  if (InsertJsonStringObj (CsJson, "@odata.type", CSPtr->odata_type) != RedfishCS_status_success) {goto Error;}

  *JsonText = (RedfishCS_char *)json_dumps(CsJson, JSON_INDENT(2 * 1) | JSON_ENSURE_ASCII);
  json_decref(CsJson);
  return RedfishCS_status_success;
Error:;
  json_decref(CsJson);
  return RedfishCS_status_unsupported;
}


  // EntryType 
  if (InsertJsonStringObj (CsJson, "EntryType", CSPtr->EntryType) != RedfishCS_status_success) {goto Error;}

  // EventId 
  if (InsertJsonStringObj (CsJson, "EventId", CSPtr->EventId) != RedfishCS_status_success) {goto Error;}

  // EventTimestamp 
  if (InsertJsonStringObj (CsJson, "EventTimestamp", CSPtr->EventTimestamp) != RedfishCS_status_success) {goto Error;}

  // EventType 
  if (InsertJsonStringObj (CsJson, "EventType", CSPtr->EventType) != RedfishCS_status_success) {goto Error;}

  // Id 
  if (InsertJsonStringObj (CsJson, "Id", CSPtr->Id) != RedfishCS_status_success) {goto Error;}

  // Links
  if (CS_To_JSON_Links(CsJson, "Links", CSPtr->Links) != RedfishCS_status_success) {goto Error;}

  // Message 
  if (InsertJsonStringObj (CsJson, "Message", CSPtr->Message) != RedfishCS_status_success) {goto Error;}

  // MessageArgs 
  if (InsertJsonStringArrayObj (CsJson, "MessageArgs", CSPtr->MessageArgs) != RedfishCS_status_success) {goto Error;}

  // MessageId 
  if (InsertJsonStringObj (CsJson, "MessageId", CSPtr->MessageId) != RedfishCS_status_success) {goto Error;}

  // Name 
  if (InsertJsonStringObj (CsJson, "Name", CSPtr->Name) != RedfishCS_status_success) {goto Error;}

  // Oem
  if (CS_To_JSON_Oem(CsJson, "Oem", CSPtr->Oem) != RedfishCS_status_success) {goto Error;}

  // OemRecordFormat 
  if (InsertJsonStringObj (CsJson, "OemRecordFormat", CSPtr->OemRecordFormat) != RedfishCS_status_success) {goto Error;}

  // SensorNumber 
  if (InsertJsonInt64Obj (CsJson, "SensorNumber", CSPtr->SensorNumber) != RedfishCS_status_success) {goto Error;}

  // SensorType 
  if (InsertJsonStringObj (CsJson, "SensorType", CSPtr->SensorType) != RedfishCS_status_success) {goto Error;}

  // Severity 
  if (InsertJsonStringObj (CsJson, "Severity", CSPtr->Severity) != RedfishCS_status_success) {goto Error;}

  // @odata.context 
  if (InsertJsonStringObj (CsJson, "@odata.context", CSPtr->odata_context) != RedfishCS_status_success) {goto Error;}

  // @odata.id 
  if (InsertJsonStringObj (CsJson, "@odata.id", CSPtr->odata_id) != RedfishCS_status_success) {goto Error;}

  // @odata.type 
  if (InsertJsonStringObj (CsJson, "@odata.type", CSPtr->odata_type) != RedfishCS_status_success) {goto Error;}

  *JsonText = (RedfishCS_char *)json_dumps(CsJson, JSON_INDENT(2 * 1) | JSON_ENSURE_ASCII);
  json_decref(CsJson);
  return RedfishCS_status_success;
Error:;
  json_decref(CsJson);

static RedfishCS_status GenMemorySet_Array_ElementCs(RedfishMemoryDomain_V1_1_0_MemoryDomain_CS *Cs, json_t *JsonObj, RedfishCS_uint64 Index,  RedfishMemoryDomain_V1_1_0_MemorySet_CS **Dst)
{
  RedfishCS_status Status;
  json_t *TempJsonObj;

  Status = RedfishCS_status_success;
  TempJsonObj = json_array_get (JsonObj, (RedfishCS_int)Index);
  if (TempJsonObj == NULL) {
    return RedfishCS_status_not_found;
  }
  Status = allocateRecordCsZeroMemory(Cs, sizeof(RedfishMemoryDomain_V1_1_0_MemorySet_CS), (RedfishCS_void **)Dst);
  if (Status != RedfishCS_status_success){
    goto Error;
  }
  // MemorySet
  InitializeLinkHead (&(*Dst)->MemorySet);
  Status = CreateCsUriOrJsonByNodeArray (Cs, TempJsonObj, "MemorySet", Cs->Header.ThisUri, &(*Dst)->MemorySet);
  if (Status != RedfishCS_status_success && Status != RedfishCS_status_not_found) {goto Error;}
  else {if (Status == RedfishCS_status_not_found){/*This is not the required property.*/}}

  // MemorySet@odata.count
  Status = GetRedfishPropertyInt64 (Cs, TempJsonObj, "MemorySet@odata.count", &(*Dst)->MemorySetodata_count);
  if (Status != RedfishCS_status_success && Status != RedfishCS_status_not_found) {goto Error;}
  else {if (Status == RedfishCS_status_not_found){/*This is not the required property.*/}}

  // MemorySet@odata.navigationLink
  Status = GetRedfishPropertyStr (Cs, TempJsonObj, "MemorySet@odata.navigationLink", &(*Dst)->MemorySetodata_navigationLink);
  if (Status != RedfishCS_status_success && Status != RedfishCS_status_not_found) {goto Error;}
  else {if (Status == RedfishCS_status_not_found){/*This is not the required property.*/}}

  return RedfishCS_status_success;
Error:;
  return Status;
}

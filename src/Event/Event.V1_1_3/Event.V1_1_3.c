static RedfishCS_status CS_To_JSON_Events(json_t *CsJson, char *Key, RedfishEvent_V1_1_3_EventRecord_Array_CS *CSPtr)
{
  json_t *ArrayJson;
  json_t *ArrayMember;
  RedfishEvent_V1_1_3_EventRecord_Array_CS *NextArray;
  RedfishEvent_V1_1_3_EventRecord_CS *NextArrayItem;

  if (CSPtr == NULL) {
    return RedfishCS_status_success;
  }
  ArrayJson = json_array();
  if (ArrayJson == NULL) {
    return RedfishCS_status_unsupported;
  }
  NextArray = CSPtr;
  do {
    ArrayMember = json_object();
    if (ArrayMember == NULL) {
      return RedfishCS_status_unsupported;
    }

    NextArrayItem = NextArray->ArrayValue;
    // Context 
    if (InsertJsonStringObj (ArrayMember, "Context", NextArrayItem->Context) != RedfishCS_status_success) {goto Error;}

    // EventId 
    if (InsertJsonStringObj (ArrayMember, "EventId", NextArrayItem->EventId) != RedfishCS_status_success) {goto Error;}

    // EventTimestamp 
    if (InsertJsonStringObj (ArrayMember, "EventTimestamp", NextArrayItem->EventTimestamp) != RedfishCS_status_success) {goto Error;}

    // EventType 
    if (InsertJsonStringObj (ArrayMember, "EventType", NextArrayItem->EventType) != RedfishCS_status_success) {goto Error;}

    // MemberId 
    if (InsertJsonStringObj (ArrayMember, "MemberId", NextArrayItem->MemberId) != RedfishCS_status_success) {goto Error;}

    // Message 
    if (InsertJsonStringObj (ArrayMember, "Message", NextArrayItem->Message) != RedfishCS_status_success) {goto Error;}

    // MessageArgs 
    if (InsertJsonStringArrayObj (ArrayMember, "MessageArgs", NextArrayItem->MessageArgs) != RedfishCS_status_success) {goto Error;}

    // MessageId 
    if (InsertJsonStringObj (ArrayMember, "MessageId", NextArrayItem->MessageId) != RedfishCS_status_success) {goto Error;}

    // Oem
    if (CS_To_JSON_EventsOem(ArrayMember, "Oem", NextArrayItem->Oem) != RedfishCS_status_success) {goto Error;}

    // OriginOfCondition
    if (CS_To_JSON_EventsOriginOfCondition(ArrayMember, "OriginOfCondition", NextArrayItem->OriginOfCondition) != RedfishCS_status_success) {goto Error;}

    // Severity 
    if (InsertJsonStringObj (ArrayMember, "Severity", NextArrayItem->Severity) != RedfishCS_status_success) {goto Error;}

    if (json_array_append_new (ArrayJson, ArrayMember) != 0) {goto Error;}
    NextArray = NextArray->Next;
  } while (NextArray != NULL);
  json_object_set_new (CsJson, Key, ArrayJson);

  return RedfishCS_status_success;
Error:;
  return RedfishCS_status_unsupported;
}
